#include<bits/stdc++.h>
using namespace std;
 int main(){
    int num = 1;
    cout<<~num<<endl;
    cout<<~(num)<<endl;
    cout<<(~num)<<endl;
    int a = 5;
    int b = 10;
    cout<<(a^b)<<endl; 
    a = 5;
    b = -5;
    cout<<(a^b)<<endl; 
    a = 5;
    b = 0;
    cout<<(a^b)<<endl; 
    cout<<(a<<1)<<endl; // left shift new value a= a*2^n; a = 10

 }